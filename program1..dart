// ## Program 1: Display Personal Information
// Write a Dart program using variables to display your name, age, school, and a hobby. Print it in one logical sentence.
void main() {
  // Personal Information
  String name = "eric kyania mulwa";
  int age = 24;
  String school = "power learn project academy";
  String hobby = "exploring new things in tech";

  // Displaying Personal Information in a Catchy Sentence with Uppercase Variables
  print(
      "In the thrilling adventures of life, meet ${name.toUpperCase()}, a daring soul of $age years, "
      "who once roamed the halls of ${school.toUpperCase()} and now conquers the skies with a passion for ${hobby.toUpperCase()}!");
}
